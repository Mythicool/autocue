// Helper functions
export const getVehicleById = (id) => {
    return inventoryData.find(vehicle => vehicle.id === id);
};

export const getFeaturedVehicles = (count = 6) => {
    return inventoryData.slice(0, count);
};

export const filterVehicles = (filters) => {
    return inventoryData.filter(vehicle => {
        if (filters.make && vehicle.make !== filters.make) return false;
        if (filters.bodyType && vehicle.bodyType !== filters.bodyType) return false;
        if (filters.minPrice && vehicle.price < filters.minPrice) return false;
        if (filters.maxPrice && vehicle.price > filters.maxPrice) return false;
        if (filters.maxMileage && vehicle.mileage > filters.maxMileage) return false;
        if (filters.certified && !vehicle.certified) return false;
        return true;
    });
};
